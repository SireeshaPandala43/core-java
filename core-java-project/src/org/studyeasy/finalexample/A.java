package org.studyeasy.finalexample;

// If we mark india() as final,it can't be overridden
//If we mark class 'A' as final, then we can't do inheritance.

public  final class A {
	
	public void india() {
		
		System.out.println("India is amazing!");
	}
	
	public void USA() {
		System.out.println("USA is fantastic! ");
	}

}
